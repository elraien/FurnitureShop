package com.furnitureshop.commontypes;

/**
 * PRODUCT_DISCOUNT - 15 % from product price
 * TOTAL_SUM_DISCOUNT - 20 % from total sum of order
 * SPECIAL_OFFER - buy 3 items pay for 2 or buy 6 items pay for 4 etc.
 */
public enum DiscountType {
    PRODUCT_DISCOUNT,
    TOTAL_SUM_DISCOUNT,
    SPECIAL_OFFER
}
